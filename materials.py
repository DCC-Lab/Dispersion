def water(wavelength):
    x = wavelength * 1e6
    if x < 0.3:
        x = 0.3
    elif x > 2.5:
        x = 2.5

    x2 = x * x
    n = (
        1
        + 5.672526103e-1 / (1 - 5.085550461e-3 / x2)
        + 1.736581125e-1 / (1 - 1.814938654e-2 / x2)
        + 2.121531502e-2 / (1 - 2.617260739e-2 / x2)
        + 1.138493213e-1 / (1 - 1.073888649e1 / x2)
    ) ** 0.5
    return n
