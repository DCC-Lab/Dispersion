def silica(wavelength):
    x = wavelength * 1e6
    if x < 0.3:
        x = 0.3
    elif x > 2.5:
        x = 2.5
    return (
        1
        + 1.03961212 / (1 - 0.00600069867 / x**2)
        + 0.231792344 / (1 - 0.0200179144 / x**2)
        + 1.01046945 / (1 - 103.560653 / x**2)
    ) ** 0.5

